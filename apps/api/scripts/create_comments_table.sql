-- Script para criar tabela de comentários do SAC
-- Execute no Oracle como PCINTEGRA ou owner apropriado
CREATE TABLE BRSACC_COMMENTS (
  ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  NUMTICKET NUMBER NOT NULL,
  CODCLI NUMBER NOT NULL,
  AUTOR VARCHAR2(200),
  TIPO_AUTOR CHAR(1) DEFAULT 'C',
  TIPO_MSG CHAR(1) DEFAULT 'M',
  -- M=Message, N=Note
  -- C = Cliente, S = Suporte
  CONTEUDO CLOB,
  ANEXO_FILENAME VARCHAR2(255),
  ANEXO_MIMETYPE VARCHAR2(100),
  ANEXO_PATH VARCHAR2(500),
  DTCRIACAO TIMESTAMP DEFAULT SYSTIMESTAMP
);
-- Índices para performance
CREATE INDEX IDX_BRSACC_COMMENTS_TICKET ON BRSACC_COMMENTS(NUMTICKET);
CREATE INDEX IDX_BRSACC_COMMENTS_CODCLI ON BRSACC_COMMENTS(CODCLI);
CREATE INDEX IDX_BRSACC_COMMENTS_DT ON BRSACC_COMMENTS(DTCRIACAO);
